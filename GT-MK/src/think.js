// تحوّل خطوة التلقين من «اضغط تمّ» إلى حوارٍ يُنمّي تفكير الطفل:
//   روبو يسأل ← الطفل يجيب ← روبو يعمّق («لماذا/كيف؟») ← اختياري: «علّمني الخطأ».
// الأجوبة شفويّةٌ للأهل (نموذج هجين بلا حُكمٍ آليّ)، فلا نطلب كتابةً من الصغار.

// سؤال تعميقٍ مفتوح بحسب المجال — يطلب من الطفل أن يُعلّل ويشرح، ويربط بالخالق حين يناسب.
const DEEPEN = {
  "علوم وطبيعة": "وفي رأيك، لِمَاذا يحدثُ هذا؟ ومَنِ الَّذي خَلَقَهُ وأَتْقَنَهُ؟",
  "أرقام وهندسة": "وكيف عرفتَ ذلك؟ أيُمكنُكَ أن تُرِيَني بطريقةٍ أُخرى؟",
  "أخلاق ومشاعر": "ولماذا هذا الخُلُقُ جميل؟ ومَنْ يُحِبُّهُ مِنْ عبادِهِ؟",
  "حروف ولغة": "أحسنت! أتذكُرُ كلمةً أُخرى تُشبهُها؟ كيف عرفتَها؟",
  "تفكير وحلّ مشكلات": "ولماذا اخترتَ هذا الحلَّ بالذّات؟ هل من حلٍّ آخرَ ممكن؟",
};

// سؤال التعميق: يُفضَّل المؤلَّف الخاصّ بالذكرى (think.why)، وإلّا فالعامّ بحسب المجال.
export function deepenFor(m) {
  return (m && m.think && m.think.why) || (m && DEEPEN[m.domain]) || "ولماذا في رأيك؟ اشرَحْ لي.";
}

// «علّمني الخطأ»: عبارةٌ يقولها روبو واثقًا وهي خاطئة، فيلتقطها الطفل ويصحّحها (تفكيرٌ نقديّ).
// تظهر فقط للذكريات التي أُلِّف لها think.robo_wrong (وإلّا فلا زرّ، حفاظًا على الجودة).
export function roboWrongFor(m) {
  return (m && m.think && m.think.robo_wrong) || null;
}
