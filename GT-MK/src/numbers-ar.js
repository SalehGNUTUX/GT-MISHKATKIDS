// التشكيل مطابقٌ تماماً لأسماء 0-10 في content/letters.js، فتُنطَق بمقاطع المجموعة المُسجَّلة (صالح)
// لا بالمقطع الآليّ — وتتّسق نطقاً مع قسم الأساسيّات. (11 فأكثر تَلجأ للمقطع الآليّ/العصبيّ.)
// مفاتيحُ الأرقام تطابقُ تشكيلَ تسجيلات «صالح» البشريّة (مطابقةٌ حرفيّة) فتُنطَق بصوته. النطقُ الآليّ/العصبيّ
// يُشكَّل عبر src/spoken-ref.js (المفتاحُ يبقى كما هو). تطابقُ أيضاً 0-10 في content/letters.js وnumerals.js.
const ONES = ["", "واحِد", "اثنان", "ثَلاثة", "أربعة", "خَمسة", "سِتّة", "سَبعة", "ثَمانية", "تِسعة"];
const TEENS = ["عَشَرة", "أَحَدَ عَشَرَ", "اثنا عَشَرَ", "ثَلاثةَ عَشَرَ", "أربعةَ عَشَرَ", "خَمسةَ عَشَرَ", "سِتّةَ عَشَرَ", "سَبعةَ عَشَرَ", "ثَمانيةَ عَشَرَ", "تِسعةَ عَشَرَ"];
const TENS = ["", "", "عِشرون", "ثَلاثون", "أربعون", "خَمسون", "سِتّون", "سَبعون", "ثَمانون", "تِسعون"];

export function numberName(n) {
  if (n === 0) return "صِفر";
  if (n === 100) return "مِئة";
  if (n < 10) return ONES[n];
  if (n < 20) return TEENS[n - 10];
  const t = Math.floor(n / 10), o = n % 10;
  return o === 0 ? TENS[t] : ONES[o] + " و" + TENS[t];
}

// قائمةُ أسماء الأعداد 0..max (افتراضاً 100) — لإدراجها في المفردات.
export function numberNames(max = 100) {
  const a = []; for (let n = 0; n <= max; n++) a.push(numberName(n)); return a;
}
