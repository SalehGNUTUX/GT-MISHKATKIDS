// تحويلٌ هجريٌّ حسابيٌّ محلّيٌّ بالكامل (خوارزميّة التقويم الهجريّ الجدوليّ الكويتيّة) — بلا إنترنتٍ ولا مكتبة.
// ملاحظةٌ صريحة: التقويمُ الجدوليُّ تقريبيٌّ وقد يختلفُ يومًا أو يومَين عن الرؤيةِ الشرعيّة؛ فهو للتهنئةِ لا للعبادة.

// تحويلُ تاريخٍ ميلاديٍّ (سنة/شهر/يوم) إلى هجريٍّ {y,m,d} عبرَ رقمِ اليومِ اليوليانيّ.
export function gregorianToHijri(gy, gm, gd) {
  let jd;
  if (gy > 1582 || (gy === 1582 && (gm > 10 || (gm === 10 && gd >= 15)))) {
    jd = Math.floor((1461 * (gy + 4800 + Math.floor((gm - 14) / 12))) / 4) +
      Math.floor((367 * (gm - 2 - 12 * Math.floor((gm - 14) / 12))) / 12) -
      Math.floor((3 * Math.floor((gy + 4900 + Math.floor((gm - 14) / 12)) / 100)) / 4) +
      gd - 32075;
  } else {
    jd = 367 * gy - Math.floor((7 * (gy + 5001 + Math.floor((gm - 9) / 7))) / 4) +
      Math.floor((275 * gm) / 9) + gd + 1729777;
  }
  let l = jd - 1948440 + 10632;
  const n = Math.floor((l - 1) / 10631);
  l = l - 10631 * n + 354;
  const j = Math.floor((10985 - l) / 5316) * Math.floor((50 * l) / 17719) + Math.floor(l / 5670) * Math.floor((43 * l) / 15238);
  l = l - Math.floor((30 - j) / 15) * Math.floor((17719 * j) / 50) - Math.floor(j / 16) * Math.floor((15238 * j) / 43) + 29;
  const m = Math.floor((24 * l) / 709);
  const d = l - Math.floor((709 * m) / 24);
  const y = 30 * n + j - 30;
  return { y, m, d };
}

